    private static abstract class TimedProvider<T> {
        protected List<T> resources;
        protected int step;
        protected int delay;
        private Thread thread;

        public TimedProvider(List<T> resources, int delay) {
            this.resources = resources;
            this.delay = delay;
        }

        public synchronized boolean isRunning() {
            return thread != null && thread.isAlive();
        }

        public synchronized void start() {
            step = 1;
            thread = new Thread(new Runnable() {
                @Override
                public void run() {
                    try {
                        onBeforeStart();
                    } catch (InterruptedException e1) {
                        onCancel();
                        return;
                    }
                    while (step < resources.size()) {
                        if (Thread.interrupted()) {
                            onCancel();
                            return;
                        }

                        T data = resources.get(step);
                        step += STEP_INTERVAL;
                        T nextData = null;
                        if (step < resources.size()) {
                            nextData = resources.get(step);
                        }
                        each(data, nextData);
                        try {
                            int randomDelay = (int)(Math.random() - 0.5) * 100; // 50ms 내외의 의도적인 오차를 준다. 
                            Thread.sleep(delay + randomDelay);
                        } catch (InterruptedException e) {
                            onCancel();
                            return;
                        }
                    }
                    try {
                        onFinish();
                    } catch (InterruptedException e) {
                        onCancel();
                        return;
                    }
                }
            });

            thread.start();
        }

        public synchronized void stop() {
            if (isRunning()) {
                thread.interrupt();
                thread = null;
            }
        }

        protected void onBeforeStart() throws InterruptedException {
        }

        protected void onCancel() {
        }

        protected void onFinish() throws InterruptedException {
        }

        protected abstract void each(T data, T nextData);
    }

    private static double speedToZoom(double speed) {
        if (speed < 20) {
            return MAX_ZOOM;
        }
        if (speed > 100) {
            return MIN_ZOOM;
        }
        int zoomRange = MAX_ZOOM - MIN_ZOOM;
        return MAX_ZOOM - (speed - 20) / 80 * zoomRange;
    }

